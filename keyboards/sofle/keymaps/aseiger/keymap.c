bool encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            tap_code(KC_VOLD);
        } else {
            tap_code(KC_VOLU);
        }
    } else if (index == 1) {
        if (clockwise) {
			switch (get_highest_layer(layer_state)) {
				case _GAMING:
				case _QWERTY:
				case _RAISE:
				case _ADJUST:
					tap_code(KC_LEFT);
					break;
				case _LOWER:
					tap_code(KC_DOWN);
					break;
			}
        } else {
            switch (get_highest_layer(layer_state)) {
				case _GAMING:
				case _QWERTY:
				case _RAISE:
				case _ADJUST:
					tap_code(KC_RIGHT);
					break;
				case _LOWER:
					tap_code(KC_UP);
					break;
			}
        }
    }
    return true;
}

